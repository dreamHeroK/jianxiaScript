function fe_AddFactionBook(nRoute,nType,nCount)
	if nType == nil then
		nType = 0;
	end;
	if nCount == nil then
		nCount = 1;
	end;
	local nTbSize = getn(Tb_Book[nRoute]);
	local nRand = random(1,nTbSize);
	local nBookIdx = 0;
	local szBookName = 0;
	local nRetCode = 0;
	if nType == 0 then
		nBookIdx = nRand;
		szBookName = Tb_Book[nRoute][nBookIdx][4];
		nRetCode = AddItem(Tb_Book[nRoute][nBookIdx][1],Tb_Book[nRoute][nBookIdx][2],Tb_Book[nRoute][nBookIdx][3],nCount);
	else
		nBookIdx = floor((nRand-1)/2)*2+nType;
		szBookName = Tb_Book[nRoute][nBookIdx][4];
		nRetCode = AddItem(Tb_Book[nRoute][nBookIdx][1],Tb_Book[nRoute][nBookIdx][2],Tb_Book[nRoute][nBookIdx][3],nCount);
	end;
	if nRetCode == 1 then
		WriteLog("[门派秘籍获取]:"..GetName().."获得"..nCount.." (本) 门派秘籍:"..szBookName);
	else
		WriteLog("[门派秘籍获取错误]:"..GetName().."获得"..nCount.."(本) 门派秘籍:"..szBookName.."AddItem 错误, 返回代码:"..RetCode);
	end;
	return szBookName;
end;
