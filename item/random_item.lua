function OnUse(nItemIndex)
	local sItemName, G, D, P = get_item_info(nItemIndex)
	local nTabIndex = 0
	for index, value in tTab do
		if value[2] == G and value[3] == D and value[4] == P then
			nTabIndex = index
			break
		end
	end
	
	if nTabIndex == 0 then
		return
	end

	style = tTab[nTabIndex][1]
	dialog = tTab[nTabIndex][6]
	UsePackToItem(PlayerIndex, nItemIndex, style, dialog)
end


function UsePackToItem(nPlayerIndex, nItemIndex, style, dialog)
	local szTitle = dialog.."确定打开吗？"
	local tbOpt = {}
		tinsert(tbOpt, {"是的", open_pack_item, {nPlayerIndex, nItemIndex, style}})
		tinsert(tbOpt, {"取消", cancel_dialog})
	CreateNewSayEx(szTitle, tbOpt)
end

function open_pack_item(nPlayerIndex, nItemIndex, style)
	PlayerIndex = nPlayerIndex
	local sItemName, g, d, p = get_item_info(nItemIndex)
	
	if gf_Judge_Room_Weight(1,100,"") ~= 1 then
		return 0
	end
	local tAward = tbAwardTab[style]
	local tTask = g_tTaskList[style]
	if (DelItem(g, d, p, 1) == 1) then
		local nDigCount = GetTask(tTask[1])
		local nNew = nDigCount + 1
		if nDigCount == 100 then
			gf_EventGiveRandAward(tAward[2].rand_award, gf_SumRandBase(tAward[2].rand_award), 1, "开启"..sItemName, sItemName)

			local szMsgName = tAward[2].rand_name
			if szMsgName ~= "" then
				local szNotice = format("恭喜%s开启%s获得[%s]×1", GetName(), sItemName, szMsgName)
				Msg2SubWorld(szNotice);
			end
			SetTask(tTask[1], 0)
		else
			gf_EventGiveRandAward(tAward[1].rand_award, gf_SumRandBase(tAward[1].rand_award), 1, "开启"..sItemName, sItemName)
			SetTask(tTask[1], nNew)
		end
	end
end

function get_item_info(nItemIndex)
	local nItemId1,nItemId2,nItemId3 = GetItemInfoByIndex(nItemIndex)
	local sItemName = GetItemName(nItemId1,nItemId2,nItemId3)
	return sItemName,nItemId1,nItemId2,nItemId3
end

