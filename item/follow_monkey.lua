function OnUse(idx)
	local nItemIndex = tonumber(idx)
	local nGenre, nDetail, nParticular = GetItemInfoByIndex(nItemIndex)
	local nTabIndex = 0
	for index, value in tTab do
		if value[1] == nGenre and value[2] == nDetail and value[3] == nParticular then
			nTabIndex = index
			break
		end
	end
	
	if nTabIndex == 0 then
		return
	end
	local sItemName, g, d, p = get_item_info(nItemIndex)
	
	npcmodel = tTab[nTabIndex][4]
	npcname = tTab[nTabIndex][5]
	dialog = tTab[nTabIndex][6] or ""

	local itemname = "<color=green>"..sItemName.."<color>："
	local szMsg = ""
	if GetFollower() ~= 0 then
		szMsg = "\n<color=red>提示：您已跟随的有跟宠了，收回后才能再次召唤<color>。"
	end
	local szTitle = itemname..dialog..szMsg
	local tbOpt = {}
		if GetFollower() ~= 0 then	--如果身后有跟随NPC，先确认删除
			tinsert(tbOpt, {"我要收回跟宠", recall_follow_pet, {PlayerIndex}})
		else
			tinsert(tbOpt, {"我要召唤跟宠", call_follow_pet, {PlayerIndex, nItemIndex, npcmodel, npcname}})
		end
		tinsert(tbOpt, {"结束对话", nothing})
	CreateNewSayEx(szTitle, tbOpt)
end

function recall_follow_pet(nPlayerIndex)
	PlayerIndex = nPlayerIndex
	KillFollower();
end;

