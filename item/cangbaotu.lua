    if P > 117 then	
	Say("花里胡哨报错了找技术",0);
	return
	end
	local nMapName = g_tTaskList[P][1]
	local nMapID = g_tTaskList[P][2]
	local nZone = 20;--控制范围
	local nWx1 = g_tTaskList[P][3]+nZone;
	local nWx2 = g_tTaskList[P][3]-nZone;	
	local nWy1 = g_tTaskList[P][4]+nZone;
	local nWy2 = g_tTaskList[P][4]-nZone;	
	local nMx = g_tTaskList[P][5]
	local nMy = g_tTaskList[P][6]
    local nPosID,nPx,nPy = GetWorldPos();
	
	if nPosID == nMapID and nPx >= nWx2 and nPx <= nWx1 and nPy >= nWy2 and nPy <= nWy1 then
		if DelItemByIndex(nItemIndex, 1) == 1 then
		tAllAward[19]:giveClause();
		end;
	 else
		Say("<color=yellow>"..nMapName.."  "..nMx.." "..nMy.."<color>",0);		
	end;
end;


