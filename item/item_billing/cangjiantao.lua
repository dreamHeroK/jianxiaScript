function OnUse(idx)
	local id = tonumber(idx)
	--物品信息的获取和整理
	local bag_diff = GetItemParticular(id) - 1058
	--流派判断
	local player_Route = GetPlayerRoute()
	if player_route_chk(player_Route) == 0 then
		Talk(1,"","你未加入门派，无法获得藏剑装备!")
		return
	elseif GetLevel() < use_level_min then
		Say("您的等级不能使用"..cangjian_bag_name[bag_diff]..",还是想获得"..cangjian_bag_name[bag_diff].."?",
			2,
			"我要使用!/#use_cangjian_bag_det("..bag_diff..")",
			"暂时不用/end_dialog"
			)
	else
		Say("你同意使用"..cangjian_bag_name[bag_diff].."来获得门派的装备?",
			2,
			"我要使用!/#use_cangjian_bag_det("..bag_diff..")",
			"暂时不用/end_dialog"
			)
	end
end
