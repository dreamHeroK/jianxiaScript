--===================================================
--使用这个物品，玩家就离线了
--离线之前在干什么，离线以后都还在干什么
--一次使用，8小时有效
--科学
--===================================================

function OnUse()
	if GetOfflineLiveTime() > 1728000 then
		Talk(1,"","你的离线时间超过480小时，暂时不能使用太白金丹！");
	elseif DelItem(2,1,202,1) == 1 then
		AddOfflineLiveTime(8 * 3600)
		Msg2Player("使用太白金丹，增加离线时间8小时")
	end
end
