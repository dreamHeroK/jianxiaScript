function onOpenBoxReal(nNum, nParticular, nOpenIndex)
	local tCfg = T_BOX[nParticular]
	if tCfg then
		for i=1,nNum do
			if 1 ~= onOpenBoxReal_Once(nParticular, nOpenIndex) then
				break
			end
		end
	end
end

