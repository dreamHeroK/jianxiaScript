function OnUse(nItemIndex)
	onUse_Confirm(nItemIndex, 0)
end

function onUse_Confirm(nItemIndex, bConfirm)
	if GetItemGenre(nItemIndex)== ITEM_G and GetItemDetail(nItemIndex)==ITEM_D then
		local nP = GetItemParticular(nItemIndex)
		local tCfg = T_BOX[nP]
		if tCfg then
			if getn(tCfg) > 1 then
				Say(format("[%s]：选择开启方式：",GetItemName(nItemIndex),getn(tCfg)),
    				2,
    				format("\n免费开启/#onUse_Real(%d,%d,%d)", nItemIndex, 1, 0),
 --   				format("\n收费开启（20个藏宝箱钥匙）/#onUse_Real(%d,%d,%d)", nItemIndex, 2, 0),
    				"\n取消/nothing");
				return
			elseif getn(tCfg) == 1 then
				onUse_Real(nItemIndex, 1, 0)
				return
			end
		end
	end
end

function onUse_Real(nItemIndex, nOpenIndex, bConfirm)
	if GetItemGenre(nItemIndex)== ITEM_G and GetItemDetail(nItemIndex)==ITEM_D then
		local nP = GetItemParticular(nItemIndex)
		local tCfg = T_BOX[nP]
		if tCfg then
			local tNeed = tCfg[nOpenIndex].tRequire
			if tNeed and 1~= bConfirm then
				Say(format("开启[%s]需要<color=yellow>%d个[%s]<color>，你确定要打开吗？",GetItemName(nItemIndex),tNeed[5], tNeed[1]),
    			2,
    			format("\n确定/#onUse_Real(%d,%d,%d)", nItemIndex, nOpenIndex, 1),
    			"\n取消/nothing");
				return
			end
			local szFunc = format("onInputNumCallBack_%d_%d",nP, nOpenIndex)
			local nMax = GetItemCount(ITEM_G, ITEM_D, nP)
			AskClientForNumber(szFunc, 1, nMax, "输入打开数量")
		end
	end
end

